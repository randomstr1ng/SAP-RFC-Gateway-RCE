def extract_p2_info(response):
    """Extract conversation ID and session PTE from F_SAP_INIT response.

    Returns:
        (conv_id, pte_bytes) where
        conv_id   -- 8-char ASCII conversation identifier (NI payload offset 40)
        pte_bytes -- 16-byte server-generated session pointer (NI payload offset 105)

    The PTE must be patched into P3 and P4 or the server returns
    "Conversation not found".  P2 response is always 125 bytes; PTEs from
    different servers differ (last 4 bytes = server IP).
    """
    if len(response) < 121:
        raise ValueError(f"INIT response too short ({len(response)} bytes, expected >=121)")
    conv_id = response[40:48].decode('ascii')
    if not conv_id.strip('\x00'):
        raise ValueError("Empty conv_id in INIT response")
    pte = bytes(response[105:121])
    return conv_id, pte


_INTER_ROW_SEP = b'\x03\x03\x03\x03'


def _decode_rows(data, num_rows, row_size=256):
    """Decode num_rows of UTF-16 LE CHAR(128) rows from raw data."""
    lines = []
    for i in range(num_rows):
        chunk = data[i * row_size:(i + 1) * row_size]
        if len(chunk) < row_size:
            break
        try:
            line = chunk.decode('utf-16-le').rstrip()
        except UnicodeDecodeError:
            line = chunk.decode('utf-16-le', errors='replace').rstrip()
        lines.append(line)
    return '\n'.join(lines)


def _extract_compressed(response, marker_idx, debug=False):
    """Handle 03 02 03 05 (SAP LZH compressed) table format.

    Block header (10 bytes after the 4-byte marker):
      [0:8]  - constant: 04 00 7b 02 00 ea 00 00
      [8:10] - stream_size (2-byte BE): byte length of the SAP compressed stream
               that immediately follows this 10-byte header.
      [10:]  - SAP compressed stream:
               [4-byte LE uncomp_len][1-byte algo=0x12][LZH data...]

    pysapcompress.decompress(sap_stream, uncomp_len) is called directly on the
    SAP stream (which already contains the length/algo header).  No prepending
    needed.  Each decompressed row = 256 bytes CHAR(128) UTF-16 LE.
    """
    if not _HAS_PYSAPCOMPRESS:
        return None

    block_start = marker_idx + 4  # skip the 4-byte marker
    if block_start + 10 > len(response):
        return None

    magic       = response[block_start:block_start + 8]
    stream_size = struct.unpack('>H', response[block_start + 8:block_start + 10])[0]

    sap_stream_start = block_start + 10
    sap_stream_end   = sap_stream_start + stream_size

    if sap_stream_end > len(response) or stream_size < 5:
        return None

    # The SAP stream starts with [4-byte LE uncomp_len][1-byte algo]...
    sap_stream = response[sap_stream_start:sap_stream_end]
    uncomp_len = struct.unpack('<I', sap_stream[0:4])[0]

    if uncomp_len == 0:
        return ''

    num_rows = uncomp_len // 256  # each row = 256 bytes CHAR(128) UTF-16 LE

    if debug:
        print(f'[DBG] Compressed block @ offset {marker_idx}:', file=sys.stderr)
        print(f'[DBG]   magic={magic.hex()}, stream_size={stream_size}', file=sys.stderr)
        print(f'[DBG]   uncomp_len={uncomp_len}, num_rows={num_rows}', file=sys.stderr)
        print(f'[DBG]   sap_stream len={len(sap_stream)}, '
              f'first bytes={sap_stream[:8].hex()}', file=sys.stderr)

    try:
        _rc, _out_len, decompressed = _sap_decompress(sap_stream, uncomp_len)
    except _SapDecompressError as e:
        if debug:
            print(f'[DBG]   DecompressError: {e}', file=sys.stderr)
        return None

    if decompressed is None:
        if debug:
            print(f'[DBG]   Decompression returned None: rc={_rc}, out_len={_out_len}',
                  file=sys.stderr)
        return None

    return _decode_rows(decompressed, num_rows)


def extract_output(response, debug=False):
    """Extract command output from F_SAP_SEND END response.

    Handles two LOG table formats:
      03 02 03 03 - uncompressed: each row is [2-byte header][256 bytes CHAR(128)]
                   with 4-byte inter-row separators between rows
      03 02 03 05 - SAP LZH compressed: 14-byte block header followed by
                   pysapcompress LZH blob; requires pysapcompress library
    """
    # --- Uncompressed format ---
    marker = b'\x03\x02\x03\x03'
    idx = response.find(marker)
    if idx >= 0:
        if idx >= 4:
            num_lines = struct.unpack('>I', response[idx - 4:idx])[0]
        else:
            num_lines = 1

        if num_lines == 0:
            return ''

        pos = idx + 4
        lines = []
        for i in range(num_lines):
            # Lines 2+ are preceded by a 4-byte inter-row separator 03 03 03 03
            if i > 0 and pos + 4 <= len(response) and response[pos:pos + 4] == _INTER_ROW_SEP:
                pos += 4
            if pos + 258 > len(response):
                break
            pos += 2  # skip 2-byte row header (01 00)
            utf16_data = response[pos:pos + 256]
            try:
                line = utf16_data.decode('utf-16-le').rstrip()
            except UnicodeDecodeError:
                line = utf16_data.decode('utf-16-le', errors='replace').rstrip()
            lines.append(line)
            pos += 256
        return '\n'.join(lines)

    # --- Compressed format (SAP LZH) ---
    comp_marker = b'\x03\x02\x03\x05'
    idx = response.find(comp_marker)
    if idx >= 0:
        return _extract_compressed(response, idx, debug=debug)

    return None


def check_response_error(response):
    """Check for error indicators in an RFC response."""
    if len(response) < 10:
        return "Response too short"

    if response[0] != 0x06:
        return f"Unexpected response version: 0x{response[0]:02x}"

    err_markers = [b'*ERR*', b'***ERROR', b'ERR_MSG', b'EXCEPTION']
    for em in err_markers:
        if em in response:
            eidx = response.find(em)
            context = response[max(0, eidx - 20):eidx + 80]
            # Detect encoding: UTF-16 LE encoded ASCII has null bytes at every
            # odd byte position (indices 1, 3, 5, …).  Check whether more than
            # half of the odd-position bytes are 0x00; if so, treat as UTF-16 LE.
            odd_bytes = context[1::2]
            if odd_bytes and sum(b == 0 for b in odd_bytes) > len(odd_bytes) // 2:
                return context.decode('utf-16-le', errors='ignore').strip('\x00').strip()
            return context.decode('ascii', errors='replace').strip('\x00').strip()

    return None


