def _patch_conn_info_npl(pkt, client_ip, target_ip):
    """Patch the client/server IPs in the P3_NPL connection-info ASCII TLV.

    The template string carries hardcoded IPs from the pcap capture session:
      fields  5 / 17  = client IP = 192.168.88.2
      fields  7 / 18  = server IP = 192.168.88.3

    Older kernels (e.g. NW 742) validate these against the actual TCP endpoints
    and reset the RFC connection with WSAECONNRESET if they do not match.

    The TLV format is: [01 27] [2-byte BE length] [ASCII string incl. NUL].
    Replacements are confined to that region; the 2-byte length prefix and the
    CPIC size fields at offsets 48 and -8 are updated to reflect any size change.
    """
    old_client = _NPL_CONN_INFO_CLIENT_IP
    old_server = _NPL_CONN_INFO_SERVER_IP
    new_client = client_ip.encode('ascii') if client_ip else old_client
    new_server = target_ip.encode('ascii') if target_ip else old_server

    if new_client == old_client and new_server == old_server:
        return pkt

    marker_off = bytes(pkt).find(b'\x01\x27')
    if marker_off < 0:
        return pkt

    len_off = marker_off + 2
    old_str_len = struct.unpack('>H', bytes(pkt[len_off:len_off + 2]))[0]
    str_start = marker_off + 4
    str_end = str_start + old_str_len

    old_str = bytes(pkt[str_start:str_end])
    new_str = old_str.replace(old_client, new_client).replace(old_server, new_server)

    if new_str == old_str:
        return pkt

    # Update the 2-byte length prefix to reflect the new string size
    struct.pack_into('>H', pkt, len_off, len(new_str))

    # Splice new string in; bytearray handles variable-length slice assignment
    pkt[str_start:str_end] = new_str

    # Adjust CPIC size fields (conn-info is inside the CPIC block)
    cpic_delta = len(new_str) - old_str_len
    if cpic_delta != 0:
        old_cpic = struct.unpack('>I', bytes(pkt[48:52]))[0]
        struct.pack_into('>I', pkt, 48, old_cpic + cpic_delta)
        old_trailer = struct.unpack('>I', bytes(pkt[len(pkt) - 8:len(pkt) - 4]))[0]
        struct.pack_into('>I', pkt, len(pkt) - 8, old_trailer + cpic_delta)

    return pkt


