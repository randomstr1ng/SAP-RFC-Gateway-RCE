def _make_th_routing_padded(sid, appserver):
    """Build 32-byte space-padded TH routing name for the SAP Transport Header.

    The routing name format is '{SID}/{appserver}_{SID}_00', which is the
    standard SAP application server registration string.  It is padded with
    spaces to exactly 32 bytes so the replacement is always length-neutral.
    If the composed name is longer than 32 bytes it is silently truncated.
    """
    name = f'{sid}/{appserver}_{sid}_00'.encode('ascii')
    return name[:32].ljust(32, b' ')


def _replace_utf16_field(pkt, old_s, new_s):
    """Replace a length-prefixed UTF-16 LE string in a CPIC parameter block.

    CPIC fields are serialised as [1-byte length][UTF-16 LE data].  This
    function locates the pattern formed by the old string's length byte followed
    by its UTF-16 LE encoding and substitutes the equivalent pattern for the
    new string.  Returns (new_pkt_bytearray, size_delta_bytes).

    A no-op (returns original pkt, delta=0) when old_s == new_s or when the
    pattern is not found.  Raises ValueError if the new UTF-16 data exceeds 255
    bytes (would overflow the single length byte).
    """
    if old_s == new_s:
        return pkt, 0
    old_u16 = old_s.encode('utf-16-le')
    new_u16 = new_s.encode('utf-16-le')
    if len(new_u16) > 255:
        raise ValueError(f"CPIC UTF-16 field too long for replacement: {new_s!r}")
    old_pat = bytes([len(old_u16)]) + old_u16
    new_pat = bytes([len(new_u16)]) + new_u16
    pb = bytes(pkt)
    count = pb.count(old_pat)
    if count == 0:
        return pkt, 0
    return bytearray(pb.replace(old_pat, new_pat)), (len(new_pat) - len(old_pat)) * count


def _patch_p3_routing(pkt, sid, appserver):
    """Patch routing info in a P3 bytearray (works for NOARGS and ARGS templates).

    Replaces:
    - TH routing name (both occurrences, always 32 bytes, length-neutral)
    - SAP SID in the CPIC connection-info ASCII string (3 chars, length-neutral)
    - CPIC UTF-16 LE parameter block (instance name, TP name, SID, hostname).
      These are the fields the remote ABAP instance validates against its own
      registration data.  Variable-length changes (instance name, hostname) are
      reflected in the CPIC size fields at template offsets 48-51 and end-8.

    Replacement order avoids double-matching:
      1. instance name (contains both appserver and SID substrings)
      2. TP name       (contains SID as prefix)
      3. standalone SID
      4. hostname      (also a prefix of the instance name)

    All replacements are no-ops when sid='A4H' and appserver='vhcala4hci'.
    """
    # --- 1. TH routing (ASCII, length-neutral, 32 bytes) ---
    new_th = _make_th_routing_padded(sid, appserver)
    pkt = bytearray(bytes(pkt).replace(_OLD_TH_ROUTING, new_th))

    # --- 2. Connection info string SID tag (ASCII, length-neutral) ---
    sid_b = sid[:3].encode('ascii')
    old_tag = b'-16-A4H-'
    new_tag = b'-16-' + sid_b + b'-'
    pkt = bytearray(bytes(pkt).replace(old_tag, new_tag))

    # --- 3. CPIC UTF-16 LE parameter block ---
    # Fields are length-prefixed: [1-byte len][UTF-16 LE data].
    # Replace in the order listed above to avoid partial matches.
    cpic_delta = 0

    pkt, d = _replace_utf16_field(pkt, 'vhcala4hci_A4H_00', f'{appserver}_{sid}_00')
    cpic_delta += d

    pkt, d = _replace_utf16_field(pkt, 'A4H-SXPG', f'{sid[:3]}-SXPG')
    cpic_delta += d  # 0 for any 3-char SID

    pkt, d = _replace_utf16_field(pkt, 'A4H', sid[:3])
    cpic_delta += d  # 0 for any 3-char SID

    pkt, d = _replace_utf16_field(pkt, 'vhcala4hci', appserver)
    cpic_delta += d

    # --- 4. Adjust CPIC size fields if packet length changed ---
    if cpic_delta != 0:
        # CPIC packet size at offset 48-51 (4-byte BE)
        old_cpic = struct.unpack('>I', pkt[48:52])[0]
        struct.pack_into('>I', pkt, 48, old_cpic + cpic_delta)
        # CPIC trailer size at end-8 (4-byte BE)
        old_trailer = struct.unpack('>I', pkt[len(pkt) - 8:len(pkt) - 4])[0]
        struct.pack_into('>I', pkt, len(pkt) - 8, old_trailer + cpic_delta)

    return pkt


