def _recv_exact(sock, n):
    """Receive exactly n bytes from socket."""
    data = b''
    while len(data) < n:
        chunk = sock.recv(n - len(data))
        if not chunk:
            raise ConnectionError("Connection closed by remote host")
        data += chunk
    return data


def ni_send(sock, payload):
    """Send NI packet: 4-byte big-endian length header + payload."""
    header = struct.pack('>I', len(payload))
    sock.sendall(header + payload)


def ni_recv(sock):
    """Receive one NI packet: read 4-byte length, then payload."""
    header = _recv_exact(sock, 4)
    length = struct.unpack('>I', header)[0]
    if length > 0x100000:
        raise ValueError(f"NI packet too large: {length} bytes")
    return _recv_exact(sock, length)


def ni_recv_handle_ping(sock):
    """Receive NI packets, auto-respond to NI_PING, return first data response."""
    while True:
        resp = ni_recv(sock)
        if resp == NI_PING:
            ni_send(sock, NI_PONG)
            continue
        return resp


