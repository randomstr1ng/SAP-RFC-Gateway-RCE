# ============================================================================
# Session token (PTE) management
#
# The 16-byte PTE (session pointer) is server-generated and returned at NI
# payload offset 105-120 in the F_SAP_INIT response.  It must match exactly
# in P3 and P4 or the server returns "Conversation not found".
#
# Templates carry hardcoded PTEs from their capture sessions:
#   P3_NOARGS + P4 : from test2 session  (A4H, PTE ends …0a141e41 = 10.20.30.65)
#   P3_ARGS        : from test4 session  (A4H, same server)
#
# At runtime the actual PTE from P2 is substituted via bytes.replace().
# The TH-header PTE (a byte-pair-swapped variant used in the SAP Transport
# header) is derived deterministically from the main PTE.
# ============================================================================

# Hardcoded PTEs baked into the templates (used as search patterns)
_NOARGS_MAIN_PTE = bytes.fromhex('6993cc9468d00c3ee10000000a141e41')
_ARGS_MAIN_PTE   = bytes.fromhex('6994dc03b3b00c7ee10000000a141e41')


def _compute_th_pte(pte):
    """Derive the TH-header PTE from the main 16-byte session PTE.

    Verified pattern across all A4H test sessions:
      th[0:4] = [pte[3]+1, pte[2], pte[1], pte[0]]   (reversed + increment)
      th[4:8] = [pte[5],   pte[4], pte[7], pte[6]]   (pair-swapped)
      th[8:]  = pte[8:]   (last 8 bytes unchanged; contain "e1 00 00 00" + IP)
    """
    return bytes([
        (pte[3] + 1) & 0xFF, pte[2], pte[1], pte[0],
        pte[5], pte[4], pte[7], pte[6],
    ]) + pte[8:]


_NOARGS_TH_PTE = _compute_th_pte(_NOARGS_MAIN_PTE)
_ARGS_TH_PTE   = _compute_th_pte(_ARGS_MAIN_PTE)

