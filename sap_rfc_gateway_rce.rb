  def adjust_q_marker(pkt, offset, delta)
    old_val = pkt.getbyte(offset + 1) | (pkt.getbyte(offset + 2) << 8)
    new_val = (old_val + delta) & 0xFFFF
    pkt[offset + 1] = [new_val & 0xFF].pack('C')
    pkt[offset + 2] = [(new_val >> 8) & 0xFF].pack('C')
  end

  def build_sap_send_start(conv_id, cmd_str, pte, sid, appserver)
    parts = cmd_str.split(' ', 2)
    program = parts[0]
