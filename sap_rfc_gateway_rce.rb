  def ni_send(payload)
    header = [payload.length].pack('N')
    sock.put(header + payload)
  end

  def ni_recv
    header = sock.timed_read(4, datastore['ConnectTimeout'] || 15)
    fail_with(Failure::Disconnected, 'Connection closed while reading NI header') if header.nil? || header.length < 4

    length = header.unpack1('N')
    fail_with(Failure::UnexpectedReply, "NI packet too large: #{length}") if length > 0x100000

    data = ''.b
    while data.length < length
      chunk = sock.timed_read(length - data.length, datastore['ConnectTimeout'] || 15)
      fail_with(Failure::Disconnected, 'Connection closed while reading NI payload') if chunk.nil? || chunk.empty?
      data << chunk
    end
    data
  end

  def ni_recv_handle_ping
    loop do
      resp = ni_recv
      if resp == NI_PING
        ni_send(NI_PONG)
        next
      end
      return resp
    end
  end

