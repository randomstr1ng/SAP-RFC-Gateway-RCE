  def patch_conn_info_npl(pkt, client_ip, target_ip)
    old_client = NPL_CONN_INFO_CLIENT_IP
    old_server = NPL_CONN_INFO_SERVER_IP
    new_client = client_ip || old_client
    new_server = target_ip || old_server

    return pkt if new_client == old_client && new_server == old_server

    marker_off = pkt.index("\x01\x27")
    return pkt if marker_off.nil?

    len_off = marker_off + 2
    old_str_len = pkt[len_off, 2].unpack1('n')
    str_start = marker_off + 4
    str_end = str_start + old_str_len

    old_str = pkt[str_start...str_end]
    new_str = old_str.gsub(old_client, new_client).gsub(old_server, new_server)

    return pkt if new_str == old_str

    pkt = pkt.dup.force_encoding('BINARY')
    pkt[len_off, 2] = [new_str.length].pack('n')
    pkt[str_start...str_end] = new_str

    cpic_delta = new_str.length - old_str_len
    if cpic_delta != 0
      old_cpic = pkt[48, 4].unpack1('N')
      pkt[48, 4] = [old_cpic + cpic_delta].pack('N')
      old_trailer = pkt[-8, 4].unpack1('N')
      pkt[-8, 4] = [old_trailer + cpic_delta].pack('N')
    end

    pkt
  end

