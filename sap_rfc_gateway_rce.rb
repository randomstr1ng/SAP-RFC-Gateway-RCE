  def compute_th_pte(pte)
    # Derive TH-header PTE from the main 16-byte session PTE
    # th[0:4] = [pte[3]+1, pte[2], pte[1], pte[0]]
    # th[4:8] = [pte[5],   pte[4], pte[7], pte[6]]
    # th[8:]  = pte[8:]
    [
      (pte.getbyte(3) + 1) & 0xFF, pte.getbyte(2), pte.getbyte(1), pte.getbyte(0),
      pte.getbyte(5), pte.getbyte(4), pte.getbyte(7), pte.getbyte(6)
    ].pack('C*') + pte[8..]
  end

