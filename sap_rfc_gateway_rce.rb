  def make_th_routing_padded(sid, appserver)
    name = "#{sid}/#{appserver}_#{sid}_00"
    name[0, 32].ljust(32, ' ')
  end

  def replace_utf16_field(pkt, old_s, new_s)
    return [pkt, 0] if old_s == new_s

    old_u16 = old_s.encode('UTF-16LE').b
    new_u16 = new_s.encode('UTF-16LE').b
    raise "CPIC UTF-16 field too long: #{new_s}" if new_u16.length > 255

    old_pat = [old_u16.length].pack('C') + old_u16
    new_pat = [new_u16.length].pack('C') + new_u16

    count = pkt.scan(old_pat).length
    return [pkt, 0] if count == 0

    result = pkt.gsub(old_pat, new_pat)
    [result, (new_pat.length - old_pat.length) * count]
  end

  def patch_p3_routing(pkt, sid, appserver)
    # 1. TH routing (ASCII, 32 bytes)
    new_th = make_th_routing_padded(sid, appserver)
    pkt = pkt.gsub(OLD_TH_ROUTING, new_th)

    # 2. Connection info SID tag
    old_tag = '-16-A4H-'
    new_tag = "-16-#{sid[0, 3]}-"
    pkt = pkt.gsub(old_tag, new_tag)

    # 3. CPIC UTF-16 LE parameter block
    cpic_delta = 0

    pkt, d = replace_utf16_field(pkt, 'vhcala4hci_A4H_00', "#{appserver}_#{sid}_00")
    cpic_delta += d

    pkt, d = replace_utf16_field(pkt, 'A4H-SXPG', "#{sid[0, 3]}-SXPG")
    cpic_delta += d

    pkt, d = replace_utf16_field(pkt, 'A4H', sid[0, 3])
    cpic_delta += d

    pkt, d = replace_utf16_field(pkt, 'vhcala4hci', appserver)
    cpic_delta += d

    # 4. Adjust CPIC size fields
    if cpic_delta != 0
      pkt = pkt.dup.force_encoding('BINARY')
      old_cpic = pkt[48, 4].unpack1('N')
      pkt[48, 4] = [old_cpic + cpic_delta].pack('N')
      old_trailer = pkt[-8, 4].unpack1('N')
      pkt[-8, 4] = [old_trailer + cpic_delta].pack('N')
    end

    pkt
  end

